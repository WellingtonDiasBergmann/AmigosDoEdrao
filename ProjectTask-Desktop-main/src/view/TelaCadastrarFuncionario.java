    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nome = jTextFieldNome.getText();
        String senha = jPasswordField.getText();
        String cpf = jTextFieldCpf.getText();
        String email = jTextFieldEmail.getText();

        if (nome != null) {
            if (senha != null) {
                if (cpf != null) {
                    if (email != null) {
                        Pessoa pessoa = new Pessoa(cpf, nome, email, senha);
                        
                        boolean cadastroSuccessful = CadastrarFuncionario(cpf, nome, email, senha);
                        
                        if(cadastroSuccessful){
                            JOptionPane.showMessageDialog(rootPane, "Funcion√°rio cadastrado com sucesso!");
                        }else{
                           jLabelErroaoCriar.setVisible(true);
                            System.out.println(pessoa);
                        }
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Por favor, preenche o email do funcion√°rio");
                        jTextFieldEmail.requestFocus();
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Por favor, preenche o cpf do funcion√°rio");
                    jTextFieldCpf.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Por favor, preenche a senha para o funcion√°rio");
                jPasswordField.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Por favor, preenche o nome do funcion√°rio");
            jTextFieldNome.requestFocus();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private boolean CadastrarFuncionario(String cpf, String nome, String email, String senha) {
        try {
            ResponseObject response = conexao.criarFuncionario(cpf, nome, email, senha);
            return response.isSuccess();
        } catch (Exception e) {
            System.err.println("Erro durante a tentativa de cadastro: " + e.getMessage());
            // Aqui voc√™ pode tamb√©m mostrar uma mensagem de erro para o usu√°rio
            return false;
        }
    }

